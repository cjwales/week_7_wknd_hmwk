<template lang="html">

</template>

<script>
export default {
  name: 'app',
  data(){
    return {
      films: []
    }
  },
  mounted() {
    fetch('https://ghibliapi.herokuapp.com/films')
    .then(res => res.json())
    .then(films => this.films = films)
  }
}
</script>

